-- ============================================
-- 5. Refresh Tokens Table (for JWT)
-- ============================================
CREATE TABLE IF NOT EXISTS refresh_tokens (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id UUID NOT NULL,
    user_type VARCHAR(20) NOT NULL, -- 'MSME', 'LENDER', 'REGULATOR'
    token VARCHAR(500) NOT NULL,
    expires_at TIMESTAMP NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    is_revoked BOOLEAN DEFAULT false
);

-- Index for faster lookups
CREATE INDEX IF NOT EXISTS idx_refresh_token ON refresh_tokens(token);
CREATE INDEX IF NOT EXISTS idx_refresh_user ON refresh_tokens(user_id, user_type);

-- ============================================
-- 6. Audit Log Table (Track all auth activities)
-- ============================================
CREATE TABLE IF NOT EXISTS auth_audit_log (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id UUID,
    user_type VARCHAR(20), -- 'MSME', 'LENDER', 'REGULATOR'
    email VARCHAR(255),
    action VARCHAR(50) NOT NULL, -- 'REGISTER', 'LOGIN', 'LOGOUT', 'PASSWORD_CHANGE'
    ip_address VARCHAR(45),
    user_agent TEXT,
    status VARCHAR(20) NOT NULL, -- 'SUCCESS', 'FAILED'
    failure_reason TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Index for audit queries
CREATE INDEX IF NOT EXISTS idx_audit_user ON auth_audit_log(user_id, user_type);
CREATE INDEX IF NOT EXISTS idx_audit_action ON auth_audit_log(action, created_at);

-- ============================================
-- 7. Update Timestamp Triggers
-- ============================================

-- Function to update updated_at timestamp
CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$ language 'plpgsql';

-- Triggers for MSME users
DROP TRIGGER IF EXISTS update_msme_users_updated_at ON msme_users;
CREATE TRIGGER update_msme_users_updated_at
    BEFORE UPDATE ON msme_users
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

-- Triggers for Lender users
DROP TRIGGER IF EXISTS update_lender_users_updated_at ON lender_users;
CREATE TRIGGER update_lender_users_updated_at
    BEFORE UPDATE ON lender_users
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

-- Triggers for Regulator users
DROP TRIGGER IF EXISTS update_regulator_users_updated_at ON regulator_users;
CREATE TRIGGER update_regulator_users_updated_at
    BEFORE UPDATE ON regulator_users
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

