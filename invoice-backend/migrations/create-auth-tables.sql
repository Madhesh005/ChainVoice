-- Authentication Tables Migration
-- Creates tables for MSME, Lender/NBFC, and Regulator users

-- ============================================
-- 1. MSME Users Table
-- ============================================
CREATE TABLE IF NOT EXISTS msme_users (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    email VARCHAR(255) UNIQUE NOT NULL,
    password_hash VARCHAR(255) NOT NULL,
    company_name VARCHAR(255) NOT NULL,
    gstin VARCHAR(15) UNIQUE NOT NULL,
    contact_person VARCHAR(255) NOT NULL,
    phone VARCHAR(20) NOT NULL,
    address TEXT,
    city VARCHAR(100),
    state VARCHAR(100),
    pincode VARCHAR(10),
    is_active BOOLEAN DEFAULT true,
    is_verified BOOLEAN DEFAULT false,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    last_login TIMESTAMP
);

-- Index for faster lookups
CREATE INDEX IF NOT EXISTS idx_msme_email ON msme_users(email);
CREATE INDEX IF NOT EXISTS idx_msme_gstin ON msme_users(gstin);

-- ============================================
-- 2. Lender/NBFC Users Table
-- ============================================
CREATE TABLE IF NOT EXISTS lender_users (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    email VARCHAR(255) UNIQUE NOT NULL,
    password_hash VARCHAR(255) NOT NULL,
    lender_name VARCHAR(255) NOT NULL,
    lender_type VARCHAR(50) NOT NULL, -- 'BANK', 'NBFC', 'FINTECH'
    license_number VARCHAR(100) UNIQUE NOT NULL,
    contact_person VARCHAR(255) NOT NULL,
    phone VARCHAR(20) NOT NULL,
    address TEXT,
    city VARCHAR(100),
    state VARCHAR(100),
    pincode VARCHAR(10),
    is_active BOOLEAN DEFAULT true,
    is_verified BOOLEAN DEFAULT false,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    last_login TIMESTAMP
);

-- Index for faster lookups
CREATE INDEX IF NOT EXISTS idx_lender_email ON lender_users(email);
CREATE INDEX IF NOT EXISTS idx_lender_license ON lender_users(license_number);

-- ============================================
-- 3. Regulator Users Table (Admin)
-- ============================================
CREATE TABLE IF NOT EXISTS regulator_users (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    email VARCHAR(255) UNIQUE NOT NULL,
    password_hash VARCHAR(255) NOT NULL,
    full_name VARCHAR(255) NOT NULL,
    role VARCHAR(50) DEFAULT 'ADMIN',
    is_active BOOLEAN DEFAULT true,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    last_login TIMESTAMP
);

-- Index for faster lookups
CREATE INDEX IF NOT EXISTS idx_regulator_email ON regulator_users(email);

